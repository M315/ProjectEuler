fn is_valid(j: usize, hs: usize) -> bool {
    hs & (1 << j) == 0
}

fn new_hash(j: usize, hs: usize) -> usize {
    hs + (1 << j)
}

fn max_sum(i: usize, hs: usize, mat: &Vec<Vec<i32>>, dp: &mut Vec<Vec<i32>>) -> i32 {
    if i >= mat.len() {
        return 0;
    }

    if dp[i][hs] != -1 {
        return dp[i][hs];
    }

    let mut best: i32 = 0;
    for j in 0..mat[i].len() {
        if is_valid(j, hs) {
            best = best.max(mat[i][j] + max_sum(i + 1, new_hash(j, hs), mat, dp));
        }
    }

    dp[i][hs] = best;
    best
}

fn matrix_sum(mat: Vec<Vec<i32>>) -> i32 {
    let mut dp: Vec<Vec<i32>> = vec![vec![-1; 1 << (mat[0].len() + 2)]; mat.len()];
    max_sum(0, 0, &mat, &mut dp)
}

